export type FrCopy = typeof fr;

/** Forme de traduction : élargit chaque littéral en `string`, chaque tableau en
 *  `readonly string[]`, et conserve la signature des fonctions d'interpolation.
 *  Permet de typer `en.ts` (et tout futur locale) sur la STRUCTURE de `fr` sans
 *  être collé à ses littéraux français — tout en imposant la parité des clés. */
type Widen<T> = T extends (...args: infer A) => infer R
  ? (...args: A) => R
  : T extends readonly unknown[]
    ? readonly string[]
    : T extends string
      ? string
      : T extends object
        ? { [K in keyof T]: Widen<T[K]> }
        : T;

export type Copy = Widen<FrCopy>;
